// Buatlah algoritma dalam bahasa Java, menjumlahkan kode ASCII dari karakter ‘X’ dan 
// karakter ‘y’!

public class MenjumlahkanKodeAscii {
    public static void main(String[] args) {
        int asciiX = 'X';
        int asciiY = 'y';

        int jumlah = asciiX + asciiY;

        System.out.println("Hasil penjumlahan kode ASCII dari karakter X dan Y adalah " + jumlah);
    }
}